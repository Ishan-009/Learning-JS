// Operators

//  < , > , = , <= ,+=
let a = 20;

console.log(a <= 15); // false
console.log(a >= 15); // true
console.log((a += 15)); // 35
console.log((a -= 15)); // 20
console.log(a === 15); // false
console.log((a *= 15)); // 300
console.log((a /= 15)); // 20
console.log(a > 15); // true
console.log(a < 15); // false
console.log(a != 15); // true
